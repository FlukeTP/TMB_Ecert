package com.tmb.payment.ws.vo;

public class CheckDocumentFile {
	
	private String fileName;
	private String docTypeCode;
	private String importDate;
	private String registrationId;
	private String refAppNo;
	private String cusName;
	private String ecmDocId;
	private String fileResCode;
	private String fileResDesc;
	private int no;
	public String getFileName() {
		return fileName;
	}
	public void setFileName(String fileName) {
		this.fileName = fileName;
	}
	public String getDocTypeCode() {
		return docTypeCode;
	}
	public void setDocTypeCode(String docTypeCode) {
		this.docTypeCode = docTypeCode;
	}
	public String getImportDate() {
		return importDate;
	}
	public void setImportDate(String importDate) {
		this.importDate = importDate;
	}
	public String getRegistrationId() {
		return registrationId;
	}
	public void setRegistrationId(String registrationId) {
		this.registrationId = registrationId;
	}
	public String getRefAppNo() {
		return refAppNo;
	}
	public void setRefAppNo(String refAppNo) {
		this.refAppNo = refAppNo;
	}
	public String getCusName() {
		return cusName;
	}
	public void setCusName(String cusName) {
		this.cusName = cusName;
	}
	public String getEcmDocId() {
		return ecmDocId;
	}
	public void setEcmDocId(String ecmDocId) {
		this.ecmDocId = ecmDocId;
	}
	public String getFileResCode() {
		return fileResCode;
	}
	public void setFileResCode(String fileResCode) {
		this.fileResCode = fileResCode;
	}
	public String getFileResDesc() {
		return fileResDesc;
	}
	public void setFileResDesc(String fileResDesc) {
		this.fileResDesc = fileResDesc;
	}
	public int getNo() {
		return no;
	}
	public void setNo(int no) {
		this.no = no;
	}
	
	
}
